package helper;

public abstract class TemperatureMonthAvg {

    private int repMonth;
    private int repYear;

    public int getRepMonth() {
        return repMonth;
    }

    public void setRepMonth(int repMonth) {
        this.repMonth = repMonth;
    }

    public int getRepYear() {
        return repYear;
    }

    public void setRepYear(int repYear) {
        this.repYear = repYear;
    }
}
